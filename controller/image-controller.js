import dotenv from "dotenv";
import mongodb from "mongodb";

const { MongoClient } = mongodb;
dotenv.config();

const username = process.env.DB_USERNAME;
const password = process.env.DB_PASSWORD;

export const uploadImage = async (req, res) => {
  try {
    const client = await MongoClient.connect(
      `mongodb+srv://${username}:${password}@cluster0.puplslt.mongodb.net`,
      { useNewUrlParser: true, useUnifiedTopology: true }
    );
    const db = client.db();
    const bucket = new mongodb.GridFSBucket(db, { bucketName: "photos" });

    // Open a stream to store the file with the same filename generated by multer
    const uploadStream = bucket.openUploadStream(req.filename);

    // Write the file data to the stream
    uploadStream.end(req.file.buffer);

    // Respond to the client
    res.status(200).json({ message: "File uploaded successfully" });
  } catch (error) {
    console.error(error);
    res.status(500).json({ error: "Internal Server Error" });
  }
};
